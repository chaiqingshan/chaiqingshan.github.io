/*类型下拉选择*/
$("#dropdown_type").on('click','td',function(e){
	$("#input_type").html("");
	$("#input_type").html($(this).html());
});
/*类型下拉悬浮背景*/
$("#dropdown_type").on('mouseover','tr',function(e){
	$(this).css("background","#f7fbff");
});
$("#dropdown_type").on('mouseout','tr',function(e){
	$(this).css("background","");
});
