/*时间正序和倒序*/
$(".iputs_down").on('click','img',function(e){
	if($(this).hasClass("down")){
		$(this).addClass("hide");
		$(".up").removeClass("hide");
	}else if($(this).hasClass("up")){
		$(this).addClass("hide");
		$(".down").removeClass("hide");
	}	
});
